#include<iostream>
using namespace std;
int main()
{
    int a;
    a=12;
    cout<<"sizeof int is "<<sizeof(a)<<endl;
    float b;
    cout<<"sizeof float is "<<sizeof(b)<<endl;
    char c;
    cout<<"sizeof char is "<<sizeof(c)<<endl;
    bool d;
    cout<<"sizeof bool is "<<sizeof(d)<<endl;
    void e;
    cout<<"sizeof void is "<<sizeof(e)<<endl;
    return 0;
}